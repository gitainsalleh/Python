#Drawing a turtle with package Turtle

import turtle

turtle.bgcolor("white") # Background color
turtle.pensize(2) # Pen Size
turtle.color("green") # Turtle color 

# Draw a square
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)

turtle.done() #Allowing drawing to stay on screen
